    public ProdutoVendasBjf viewBean() {
        ProdutoVendasBjf produtovendasBjf = new ProdutoVendasBjf();
        int quantidade = Util.strInt(jFmtQuantiBjf.getText());
        double valorU = Util.strDouble(jFmtValorUniBjf.getText());
        produtovendasBjf.setProdutoBjf((ProdutoBjf) jCboProduto.getSelectedItem());
        produtovendasBjf.setValorUnitarioBjf(valorU);
        produtovendasBjf.setQuantidadeBjf(quantidade);
        
        return produtovendasBjf;
    }
    
    public void beanView(ProdutoVendasBjf produtoVendasBjf) {
        String intStr = Util.intStr(produtoVendasBjf.getQuantidadeBjf());
        String total = Util.doubleStr(produtoVendasBjf.getValorUnitarioBjf());
        jCboProduto.setSelectedItem(produtoVendasBjf.getProdutoBjf());
        jFmtQuantiBjf.setText(Util.intStr(produtoVendasBjf.getQuantidadeBjf()));
        jFmtValorUniBjf.setText(Util.doubleStr(produtoVendasBjf.getValorUnitarioBjf()));
    }
    
