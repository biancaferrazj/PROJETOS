    private void jBtnOkBjfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnOkBjfActionPerformed
        ProdutoVendasBjf produtovendasbjf = new ProdutoVendasBjf();
        VendasProdutoControleBjf vendasprodutocontrole = new VendasProdutoControleBjf();
        produtovendasbjf.setProdutoBjf((ProdutoBjf) jCboProduto.getSelectedItem());
        produtovendasbjf.setQuantidadeBjf(Util.strInt(jFmtQuantiBjf.getText()));
        produtovendasbjf.setValorUnitarioBjf(Util.strDouble(jFmtValorUniBjf.getText()));
        if (getTitle().toUpperCase().substring(0, 1).equals("I")) {
            jDlgVendas.vendaprodutoControleBjf.addBean(produtovendasbjf);
        } else {
            jDlgVendas.vendaprodutoControleBjf.updateBean(jDlgVendas.getSelectedRowProd(), produtovendasbjf);
        }
        Util.limparCampos(jFmtQuantiBjf);
        setVisible(false);        // TODO add your handling code here:
    }//GEN-LAST:event_jBtnOkBjfActionPerformed

