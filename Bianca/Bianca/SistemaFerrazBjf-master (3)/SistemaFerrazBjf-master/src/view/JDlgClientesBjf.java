    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed
        ClienteBjf clienteBjf = viewBean();
        ClientesDAO clientesDAOBjf = new ClientesDAO();

        if (incluir == true) {

            clientesDAOBjf.insert(clienteBjf);
            Util.mensagem("Registro incluido com sucesso.");

        } else {

            clientesDAOBjf.update(clienteBjf);
            Util.mensagem("Registro alterado com sucesso.");

        }

        Util.habilitar(false, jTxtPaisBjf, jBtnConfirmar, jBtnCancelar, jFmtCodigo, jBtnAlterar, jBtnCancelar, jBtnConfirmar, jBtnExcluir, jTxtNomeBjf, jTxtNomeBjf, jFmtDataNasc, jTxtEmail, jCboEstado, jFmtTelefoneBjf, jFmtRGBjf, jFmtCPFBjf, jTxtProfissao, jTxtEndereço, jTxtEstado, jTxtCidade, jFmtCEP, jFmtDataRegistro);
        Util.limparCampos(jTxtPaisBjf, jTxtNomeBjf, jTxtNomeBjf, jFmtDataNasc, jTxtEmail, jCboEstado, jFmtTelefoneBjf, jFmtRGBjf, jFmtCPFBjf, jTxtProfissao, jTxtEndereço, jTxtEstado, jTxtCidade, jFmtCEP, jFmtDataRegistro);
        Util.habilitar(true, jBtnIncluir, jBtnPesquisar);
    }//GEN-LAST:event_jBtnConfirmarActionPerformed

