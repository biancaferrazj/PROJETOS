    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
        if (Util.perguntar("Deseja excluir o registro?") == true) {

            ClienteBjf clienteBjf = viewBean();
            ClientesDAO clientesDAO = new ClientesDAO();
            clientesDAO.delete(clienteBjf);
            Util.mensagem("Registro Excluido.");

        } else {

            Util.mensagem("Exclusão Cancelada.");

        }

        Util.limparCampos(jTxtPaisBjf, jFmtCodigo, jTxtNomeBjf, jTxtNomeBjf, jFmtDataNasc, jTxtEmail, jCboEstado, jFmtTelefoneBjf, jFmtRGBjf, jFmtCPFBjf, jTxtProfissao, jTxtEndereço, jTxtEstado, jTxtCidade, jFmtCEP, jFmtDataRegistro);
    }//GEN-LAST:event_jBtnExcluirActionPerformed

