    private void jBtnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConsultarActionPerformed
        if (jTxtNomeBjf.getText().isEmpty() && jTxtPrecoBjf.getText().isEmpty()) {
            List lista = produtoDAO.listNome(jTxtNomeBjf.getText());
            produtoControleBjf.setList(lista);
        } else {
            if (!jTxtNomeBjf.getText().isEmpty() && !jTxtPrecoBjf.getText().isEmpty()) {
                double preco = Double.parseDouble(jTxtPrecoBjf.getText());
                List lista = produtoDAO.listNomePreco(jTxtNomeBjf.getText(), preco);
                produtoControleBjf.setList(lista);
            } else {
                if (!jTxtNomeBjf.getText().isEmpty()) {
                    List lista = produtoDAO.listNome(jTxtNomeBjf.getText());
                    produtoControleBjf.setList(lista);
                }
                if (!jTxtPrecoBjf.getText().isEmpty()) {
                    double preco = Double.parseDouble(jTxtPrecoBjf.getText());
                    List lista = produtoDAO.listPreco(preco);
                    produtoControleBjf.setList(lista);
                }
            }
        }
    }//GEN-LAST:event_jBtnConsultarActionPerformed

